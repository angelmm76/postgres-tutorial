import psycopg2
import sys

con = None

try:
###### Connect to database
    con = psycopg2.connect(database='dvdrental', 
                           user='postgres', password='postgres',
                           host = 'localhost', port = "5432") 
    print("Opened database successfully")
###### Get cursor
    cur = con.cursor()
# Null is unknown or missing information, is different form empty or zero
    cur.execute("""CREATE TABLE invoice(id serial PRIMARY KEY,
          product_id int NOT NULL, qty numeric NOT NULL CHECK(qty > 0),
          net_price numeric CHECK(net_price > 0))""")
    cur.execute("INSERT INTO invoice (qty, net_price) VALUES (5, 27)") # Error!

##### Fetch all queried rows
    rows = cur.fetchall()
    print("\nShow queried data:")
    print([desc[0] for desc in cur.description])# headers
    for row in rows:
        print(row)
        
####### Close communication
#    cur.close()
####### Commit te changes
#    con.commit()
    
except psycopg2.DatabaseError as e:
    print('Error %s' % e)
    sys.exit(1)
      
finally:
###### Close connection
    if con:
        con.close()